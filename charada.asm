	rts



; comecar_charada
; r1 -> Banner com a pergunta
; r2 -> Resposta correta
; r6 -> Dica para a charada
comecar_charada:
	call imprime_charada
	call aguarda_resposta
	call verifica_resposta	
	rts



; Verifica se a resposta é a correta
verifica_resposta:
	push r1
	push r2
	push r3
	push r5
	
	load r1, Resposta
	
	; A string digitada e a resposta
	; são convertidas para letras maiúsculas o que torna as respostas case-insensitive
	load r0, Resposta
	call upper_string
	
	loadi r0, r2
	call upper_string
	
	; Chama a função de comparação que armazena o resultado na variável <RespostaCorreta>
	call compara_strings
	
	; Se a resposta estiver errada
	; entao o programa finaliza
	load r1, RespostaCorreta
	loadn r2, #1
	cmp r1, r2
	cne resposta_errada_fim
	
	
	pop r5
	pop r3
	pop r2
	pop r1
	rts


; Printa a tela de derrota do jogo
; e faz um halt
resposta_errada_fim:
	push r0
	push r1
	push r2
	
	loadn r1, #TelaFim0
	loadn r2, #2304
	loadn r6, #30
	loadn r0, #0
	call imprime_tela
	 
	pop r2
	pop r1
	pop r0
	
	halt
	
	
; Printa a tela de vitória do jogo
; e faz um halt
resposta_correta_fim:
	push r0
	push r1
	push r2
	
	loadn r1, #TelaVitoria0
	loadn r2, #512
	loadn r6, #30
	loadn r0, #0
	call imprime_tela
	 
	pop r2
	pop r1
	pop r0
	
	halt


; Printa a tela contendo a dica
; da charada
; r6 -> dica para ser printada
mostra_dica:
	push r1
	
	mov r1, r6
	call imprime_charada
	
	pop r1
	
	; É necessário apagar um caracter pois o usuário para mostrar a dica deve apertar <9>.
	jmp del_char

; Printa a charada referenciada na variável <CharadaAtual>
; e deleta um caractere pelo mesmo motivo: o usuário aperta <8> para voltar para a charada.
mostra_charada:
	load r1, CharadaAtual
	call imprime_charada
	jmp del_char
	

; Funcao que deleta os caracteres conforme <backSpace> eh pressionado
del_char:
	push r1
	
	; Limita até onde o cursor pode ir apagando as teclas anteriores.
	; Nesse caso, na posição 1054
	loadn r1, #1054
	cmp r1, r0
	jeq del_char_fim
	
	dec r0
	dec r3
	loadn r1, #0
	outchar r1, r0	
	
	; Atualiza a variável <Resposta> para colocar zero no lugar do caractere apagado.
	storei r3, r1
	
del_char_fim:
	pop r1
	jmp aguarda_resposta_loop

aguarda_resposta:
	push r0
	push r1
	push r2
	push r3
	push r4
	push r5
	push r6
	push r7

	loadn r0, #1054   ; Posição na tela
	loadn r2, #13     ; Caractere de parada <Enter>
	loadn r4, #08     ; Código ascii da tecla <BackSpace>. O código do simulador foi alterado para identificar a tecla <BackSpace>
	loadn r5, #'9'    ; Tecla para mostrar a tela de dica
	loadn r7, #'8'	  ; Tecla para voltar para a charada original
	load r3, Resposta ; buffer de resposta
	store CharadaAtual, r1 ; Variável que guarda a charada atual

aguarda_resposta_loop:
	call getchar
	load r1, LetraInstrucao
	
	cmp r1, r2
	jeq aguarda_resposta_fim
	
	cmp r1, r4
	jeq del_char
	
	cmp r1, r5
	jeq mostra_dica
	
	cmp r1, r7
	jeq mostra_charada
	
	outchar r1, r0
	storei r3, r1
	inc r0
	inc r3
	jmp aguarda_resposta_loop


aguarda_resposta_fim:
	loadn r0, #'\0'
	storei r3, r0
	
	pop r7
	pop r6
	pop r5
	pop r4
	pop r3
	pop r2
	pop r1
	pop r0
	rts

; IMPRIME CHARADA
; r1 -> InicioDaCharad0
; tamanho padrao -> 23 linhas
; cor padrao -> verde -> 512

imprime_charada:
	push r0
	push r1
	push r2
	push r6
	
	loadn r0, #0    ; Posição na tela que começa a impressão (em linhas)
	loadn r2, #512  ; Cor
	loadn r6, #23   ; Número de linhas que o desenho ocupa.
	call imprime_tela

	loadn r0, #23   ; Posição na tela que começa a impressão (em linhas)
	loadn r1, #RespostaBanner0 ; Área em que o usuário digita a resposta
	loadn r2, #1280 ; Cor
	loadn r6, #7    ; Número de linhas que o desenho ocupa.
	call imprime_tela
	
	pop r6
	pop r2
	pop r1
	pop r0
	rts




; Aguarda a tecla <Enter> ser pressionada
AguardaEnter:
	call getchar
	load r0, LetraInstrucao
	loadn r1, #13
	cmp r0, r1
	jne AguardaEnter
	rts





; - - - Funcoes auxiliares - - - 
; Lê uma letra do teclado e joga na
; variável <LetraInstrucao>
getchar:
	push r0
	push r1
	loadn r1, #255

getchar_loop:
	inchar r0
	cmp r0, r1
	jeq getchar_loop

getchar_fim:
	store LetraInstrucao, r0
	pop r1
	pop r0
	rts


; Função imprime tela
;	r0 -> linha onde o desenho começa na tela	
;	r1 -> ponteiro do início do desenho
;	r2 -> cor do desenho
;	r6 -> número de linhas que o desenho ocupa

imprime_tela:
	push r0
	push r1
	push r2
	push r3
	push r4
	push r5
	push r6
	
	loadn r3, #40 ; incremento de 40 pois cada linha tem 40 caracteres
	loadn r4, #41
	mul r0, r0, r3
	mul r5, r6, r3 ; fim do desenho
	add r5, r5, r0

imprime_tela_loop:
	call imprime_string
	add r0, r0, r3
	add r1, r1, r4
	cmp r0, r5
	jne imprime_tela_loop

imprime_tela_fim:
	pop r6
	pop r5
	pop r4
	pop r3
	pop r2
	pop r1
	pop r0
	rts


; Compara strings
; r1 -> string1
; r2 -> string2
; O resultado é armazenado na variável <RespostaCorreta>
compara_strings:
	push r1
	push r2

	push r3
	push r4
	
	push r5
	loadn r5, #'\0'

compara_strings_loop:
	loadi r3, r1
	loadi r4, r2
	
	cmp r3, r4
	jne compara_falso
	
	cmp r3, r5
	jeq compara_true
	
	inc r1
	inc r2
	jmp compara_strings_loop


compara_falso:
	loadn r1, #0
	store RespostaCorreta, r1
	jmp compara_fim_loop

compara_true:
	loadn r1, #1
	store RespostaCorreta, r1
	jmp compara_fim_loop

compara_fim_loop:
	pop r5
	pop r4
	pop r3
	pop r2
	pop r1
	rts


; Imprime string
imprime_string:  
	push r0
	push r1
	push r2
	push r3
	push r4
	
	loadn r3, #'\0'

imprime_string_loop:
	loadi r4, r1
	cmp r4, r3
	jeq imprime_string_fim
	add r4, r2, r4
	outchar r4, r0
	inc r0
	inc r1
	jmp imprime_string_loop

imprime_string_fim:
	pop r4
	pop r3
	pop r2
	pop r1
	pop r0
	rts


; Converte todos os caracteres da string para maiúsculo
; r0 -> string
upper_string:
	push r1
	push r2
	push r3
	push r5

	loadn r2, #'\0' ; Caractere que indica o final da string
	loadn r3, #97   ; Primeiro caractere na tabela ascii de letras maiúsculas
	loadn r5, #32	; Diferença entre os caracteres de letra minúscula e maiúscula

upper_loop:
	loadi r1, r0
	
	cmp r1, r2
	jeq upper_done
	
	; Se o caractere for maior ou igual a 97 entao não é necessário somar
	cmp r1, r3
	jeg upper_continue
	
	; Se o caractere estiver abaixo, então a conversão é feita
	add r1, r1, r5
	storei r0, r1

upper_continue:
	inc r0
	jmp upper_loop
	
upper_done:
	pop r5
	pop r3
	pop r2
	pop r1
	rts









	
	

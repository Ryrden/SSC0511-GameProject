main:
	
	loadn r1, #TelaInicial0
	loadn r6, #23 ; tamanho da tela
	loadn r2, #512 ; COR Verde
	loadn r0, #0
	call imprime_tela
	
	loadn r0, #23
	loadn r1, #TelaEmbaixo0
	loadn r2, #1280
	loadn r6, #7
	call imprime_tela

	; Aguarda o usuário apertar enter para começar
	call AguardaEnter
	call IniciarJogo
	call resposta_correta_fim
	
	halt




; Função responsável por iniciar o jogo
IniciarJogo:
	push r1
	push r2
	
	loadn r1, #PrimeiraCharada0
	loadn r2, #PrimeiraCharadaResposta
	loadn r6, #PrimeiraCharadaDica0
	call comecar_charada
	
	loadn r1, #SegundaCharada0
	loadn r2, #SegundaCharadaResposta
	loadn r6, #SegundaCharadaDica0
